function parseQuestions(text) {
  const questions = [];
  const lines = text.split('\n\n').filter(line => line.trim());
  
  for (const block of lines) {
    const lines = block.split('\n');
    let questionText = '';
    let hindiText = '';
    const options = [];
    let correctAnswer = -1;
    
    for (const line of lines) {
      if (line.includes('Ans:')) {
        const ansMatch = line.match(/Ans:\s*([A-D])/);
        if (ansMatch) {
          correctAnswer = ansMatch[1].charCodeAt(0) - 65; // A=0, B=1, C=2, D=3
        }
      } else if (line.match(/^[A-D]\.\s/)) {
        const option = line.replace(/^[A-D]\.\s*/, '').trim();
        options.push(option);
      } else if (line.includes('(') && line.includes(')')) {
        const match = line.match(/^(.+?)\s*\((.+?)\)/);
        if (match) {
          questionText = match[1].trim();
          hindiText = match[2].trim();
        } else {
          questionText = line.trim();
        }
      } else if (line.trim() && !line.includes('Ans:')) {
        questionText = line.trim();
      }
    }
    
    if (questionText && options.length === 4 && correctAnswer >= 0) {
      // Create Hindi options (simplified - using same text for now, but you can enhance this)
      const options_hi = options.map(opt => {
        // Try to extract Hindi if present in parentheses
        const hindiMatch = opt.match(/\((.+?)\)/);
        return hindiMatch ? hindiMatch[1] : opt;
      });
      
      questions.push({
        question_en: questionText,
        question_hi: hindiText || questionText,
        options_en: options,
        options_hi: options_hi,
        correctAnswer: correctAnswer,
        marks: 1,
        explanation_en: `The correct answer is ${String.fromCharCode(65 + correctAnswer)}. ${options[correctAnswer]}`,
        explanation_hi: `सही उत्तर ${String.fromCharCode(65 + correctAnswer)} है। ${options[correctAnswer]}`
      });
    }
  }
  
  return questions;
}

